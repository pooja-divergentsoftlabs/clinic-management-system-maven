package com.divergentsl.clinicmanagementsystem.dto;

public class PatientDto {

	String pid, pname, paddress, pcontactnumber, page, pweight;

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pname) {
		this.pname = pname;
	}

	public String getPaddress() {
		return paddress;
	}

	public void setPaddress(String paddress) {
		this.paddress = paddress;
	}

	public String getPcontactnumber() {
		return pcontactnumber;
	}

	public void setPcontactnumber(String pcontactnumber) {
		this.pcontactnumber = pcontactnumber;
	}

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getPweight() {
		return pweight;
	}

	public void setPweight(String pweight) {
		this.pweight = pweight;
	}

	
}
